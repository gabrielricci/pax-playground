static int GetSystemTime(char *psSystemTime)
{
    long int  	tnow;
    struct tm	*mytm;

    time(&tnow);

    mytm = localtime(&tnow);

    sprintf(psSystemTime, "%4d%02d%02d%02d%02d%02d",
    		mytm->tm_year+1900, mytm->tm_mon+1, mytm->tm_mday, mytm->tm_hour, mytm->tm_min, mytm->tm_sec);

    return 0;
}

static int EMVProcTrans(void)
{
	unsigned long ulAuthAmt, ulCashBackAmt;
	int iRet, iAuthCodeLen, iAuthDataLen, iScriptLen, iCommuStatus;
	unsigned char ACType;
	unsigned char RspCode[3], AuthCode[7],AuthData[17], Script[256];

	ulAuthAmt = 1;
	ulCashBackAmt = 0;
	iRet = EMVStartTrans(ulAuthAmt, ulCashBackAmt, &ACType);
	if(iRet)
	{
		return iRet;
	}

	if(ACType == AC_TC)
	{
		return EMV_OK;
	}

	if(ACType == AC_AAC)
	{
		return EMV_DENIAL;
	}

	iAuthCodeLen = iAuthDataLen = iScriptLen = 0;
	memset(RspCode,  0, sizeof(RspCode));
	memset(AuthCode, 0, sizeof(AuthCode));
	memset(AuthData, 0, sizeof(AuthData));
	memset(Script,	 0, sizeof(Script));

	iRet = cEMVOnlineProc(RspCode, AuthCode, &iAuthCodeLen, AuthData, &iAuthDataLen, Script, &iScriptLen);

	if(ONLINE_APPROVE == iRet)
	{
		iCommuStatus = ONLINE_APPROVE;
	}
	else if(ONLINE_ABORT == iRet)
	{
		iCommuStatus = ONLINE_FAILED;
	}
	else
	{
		iCommuStatus = ONLINE_DENIAL;
	}

	if(iAuthCodeLen == 2)
	{
		EMVSetTLVData(0x8A, AuthCode, 2);
	}
	else
	{
		return EMV_DENIAL;
	}

	if(iAuthDataLen != 0)
	{
		EMVSetTLVData(0x89, AuthData, iAuthDataLen);
	}

	ACType = 0;
	iRet = EMVCompleteTrans(iCommuStatus, Script, &iScriptLen, &ACType);
	if(iRet)
	{
		return iRet;
	}

	return 0;
}

